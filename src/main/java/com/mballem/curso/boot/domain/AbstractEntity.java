package com.mballem.curso.boot.domain;

import java.io.Serializable;

import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.MappedSuperclass;

@SuppressWarnings("serial")
@MappedSuperclass //Dizer ao JPA que essa é uma superclass das classes que vamos implementar
public abstract class AbstractEntity<ID extends Serializable> implements Serializable { //Definido como genérico, pois as classes filhas que serão responsaveis por definir tipo elas irão utilizar como chave primaria
																			   //Foi implementado o serializable pois é uma boa prática quando trabalhamos com framework ORM, transformar todas as classes num tipo serializable
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	private ID id;

	public ID getId() {
		return id;
	}

	public void setId(ID id) {
		this.id = id;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		AbstractEntity<?> other = (AbstractEntity) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "id= " + id;
	}
	
}
